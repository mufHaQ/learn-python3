#!/usr/bin/env python3

# Keyword Arguments: Arguments preceded by an identifier when we pass the to a function. The order of arguments doesn't matter, unlike positional arguments, Python know the names of the arguments that our function receive.

def hello(first_name, last_name):
    print("Hello " + first_name + " " + last_name)

hello(last_name="Mufliansyah", first_name="Dliyaulhaq")
